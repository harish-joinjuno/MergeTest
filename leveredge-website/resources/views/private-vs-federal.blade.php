                <h2>Private Student Loans</h2>
                <p>
                    Private student loans are offered by private banks and lenders. These loans are available to finance your degree upfront and can also be used to refinance both private and federal loans after graduation. To apply for these loans, students must apply directly through the various lender websites.
                </p>
                <p>
                    <strong>How are private loan interest rates set?</strong> Every private lender has their own unique <a target="_blank" href="https://www.investopedia.com/terms/u/underwriting.asp">underwriting</a> process and standards for student loan applicants; these eligibility requirements help lenders decide whether to give an applicant a loan, and at what interest rate. As part of the loan application process, lenders will require a credit check to evaluate your ability to repay and how risky you are. If you have a credit score in the high 600s, you will likely qualify for a loan, and the higher your score is, the better rate you will be offered.
                </p>
                <p>
                    After you submit an application and receive approval for a private student loan, you
                    typically will be presented with multiple options such as:
                </p>
                <ul>
                    <li>Variable or Fixed Interest rates</li>
                    <li>Loan Term (also known as Repayment Term)</li>
                    <li>Repayment Plan</li>
                </ul>
