            <h2>How did we run the numbers?</h2>
            <div class="separator"></div>

            <ul class="mt--3">
                <li>
                    Step 1:	Look at nominal tuition and mandatory fees for each school, as reported by <a href="https://nces.ed.gov/ipeds/datacenter/InstitutionByName.aspx?stepId=1">IPEDS</a>. This includes data through the 2017-18 academic year.
                </li>

                <li>
                    Step 2:	Adjust historical tuition + fees for 2019 dollars. Multiply nominal data by inflation factor. Specifically, anchor on 2019, look at <a href="https://www.bls.gov/">Bureau of Labor Statistics data</a> on what a dollar in 2018 is worth in 2019, for example. Repeat the process for all previous years.
                </li>

                <li>
                    Step 3:	Now that you have inflation adjusted tuition + fees, calculate the compound annual growth rate (CAGR) over the past 10 years. Mechanically, the formula is (Final Year Tuition / First Year Tuition)^(1/10)-1. You can also do this over a 3 year period.
                </li>

                <li>
                    Step 4:	Project future costs. We've already adjusted for inflation in the previous step, so the formula is (2019 tuition + fees) X (1 + Inflation Adjusted CAGR)^(t years in the future).
                </li>
            </ul>
