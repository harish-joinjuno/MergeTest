            <h2>Eligibility Requirements</h2>
            <div class="separator"></div>

            <p class="mt--3">
                Based on the surveys of Juno Members in the Spring 2019 Refinance Negotiation Group, we believe that a vast majority of Juno Members meet the eligibility criteria for Earnest Refinance loans. The following information has been copied from: <a href="https://www.earnest.com/eligibility">https://www.earnest.com/eligibility</a> for your convenience.
            </p>

            <h3 class="mt-4">PERSONAL</h3>

            <ul class="mt-3">
                <li>You are a U.S. citizen or possess a 10-year (non-conditional) Permanent Resident Card.</li>
                <li>Your current enrollment status is less than half-time and your student loans are in repayment, or your degree will be complete at the end of this semester.</li>
                <li>You currently reside in the District of Columbia or one of the 47 states we lend in (all but Delaware, Kentucky, and Nevada). *Please note, we are not able to offer variable rate loans in AK, IL, MN, NH, OH, TN, and TX.</li>
                <li>You are at least 18 years old</li>
            </ul>

            <h3 class="mt-4">LOAN</h3>
            <ul class="mt-3">
                <li>You are currently the primary borrower on the student loans you would like to refinance.</li>
                <li>You are not requesting new loans for future schooling.</li>
                <li>The debt is from paying for a Title IV-accredited school.</li>
                <li>The debt is from your own education.</li>
                <li>The debt youâ€™re refinancing is from one or more completed degrees or a degree that will be completed by the end of this semester.</li>
                <li>You are requesting to refinance at least $5000 in student loans.</li>
                <li>You are not requesting to refinance an existing Earnest loan without at least 6 months of on-time payments.</li>
            </ul>

            <h3 class="mt-4">FINANCIAL</h3>

            <ul class="mt-3">
                <li>You have a minimum credit score of 650.</li>
                <li>You are employed, have a written job offer for a position that starts within 6 months, or possess consistent income (in USD).</li>
                <li>Your student loan accounts are all in good standing.</li>
                <li>You are current on your rent or mortgage payments.</li>
                <li>You do not have a bankruptcy on your credit report or accounts recently in collection.</li>
            </ul>

            <h3 class="mt-4">Factors for Approval</h3>

            <ul class="mt-3">
                <li>You have enough savings to cover at least two months of normal expenses, including housing.</li>
                <li>You spend less than you earn and have increasing bank account balances.</li>
                <li>You do not carry large amounts of non-student, non-mortgage debt (e.g. credit cards, personal loans).</li>
                <li>You have a history of making payments on time.</li>
                <li>You are not regularly charged late, overdraft, or insufficient funds fees.</li>
            </ul>

